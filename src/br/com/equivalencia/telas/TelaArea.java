    //criando o metodo adicionar
    private void adicionar(){
        String sql = "insert into tb_area_tecnologica(nome_area) values(?)";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            
            //validando campos de preenchimento obrigatorio
            if (txtNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "campos de preechimento obrigatorio esta em branco");
           
            }else{
                int adicionado = pst.executeUpdate();
               
                if (adicionado > 0){
                    JOptionPane.showMessageDialog(null, "area tecnologica cadastrada com sucesso");
                txtNome.setText(null);
                txtNome.requestFocus();
                
               
                }
               
            }
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
        }
        
    } 
    private void alterar(){
        String sql = "updare tb_area_tecnologica set nome_area=? where id_area=?";
        try {
            pst=conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtidarea.getText());
           
            //validaÃ§ao dos campos obrigatorios 
            if (txtidarea.getText().isEmpty() || (txtNome.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "campos de preenchimento obrigatorio em branco");
            }else{
                int adicionado = pst.executeUpdate();
                if (adicionado > 0){
                    JOptionPane.showMessageDialog(null, "area tecnologica ");
                }
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    //metodo de pesquisa area tecnologica no banco de dados 
    private void pesquisar_area() {
        String sql = "select id_area, nome_area from tb_area_tecnologica where nome_area like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,"&" + txtPesquisaArea.getText()+"&");
            rs = pst.executeQuery();
            tblArea.setModel(DbUtils.resultSetTotaltableMode1(rs));
            
        } catch (Exception e) {
          JOptionPane.showMessageDialog(null, e);
        }
    }
    
     private void setar_campos(){
         int setar = tblArea.getSelectedRow();
         txtidarea.setText(tblArea.getModel().getValueAt(setar, 0).toString());
         txtNome.setText(tblArea.getModel().getValueAt(setar, 1).toString());
     }
        

